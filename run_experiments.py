import json
import random

def load_and_convert_graph(json_file_path, num_processors=4, seed=None):
    """
    Reads a graph generated by graph_generator.py and adapts it for PEFT/HEFT.
    """
    if seed is not None:
        random.seed(seed)
        
    with open(json_file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    processors = [f'core_{i}' for i in range(num_processors)]
    tasks = {}
    
    for task_info in data['tasks']:
        raw_id = task_info['id']
        # Convert 'task1' to 'T1' to match PEFT/HEFT sorting expectations
        if raw_id.startswith('task'):
            task_id = 'T' + raw_id[4:]
        else:
            task_id = raw_id
            
        t_duration = task_info['duration']
        
        # 1. Simulate Heterogeneous Computation Costs (±30% variation)
        comp_costs = {}
        for p in processors:
            variation = random.uniform(0.7, 1.3)
            comp_costs[p] = round(t_duration * variation, 2)
            
        # 2. Simulate Communication Costs
        dependencies = {}
        for dep_id in task_info.get('dependencies', []):
             if dep_id.startswith('task'):
                 norm_dep = 'T' + dep_id[4:]
             else:
                 norm_dep = str(dep_id)
                 
             # Random communication delay between 5.0 and 20.0
             dependencies[norm_dep] = round(random.uniform(5.0, 20.0), 2)
             
        tasks[task_id] = {
            'comp_costs': comp_costs,
            'dependencies': dependencies
        }
        
    converted_data = {
        'processors': processors,
        'tasks': tasks
    }
    return converted_data

if __name__ == '__main__':
    from peft_scheduler import PEFT
    from heft_scheduler import HEFT
    
    # 1. Load and Adapt json data
    # (Here we use 'graph.json', which has 9 tasks)
    print("Loading graph.json and converting data format...")
    dag_data = load_and_convert_graph('graph.json', num_processors=4, seed=42)
    
    # 2. Run PEFT
    print("\n[1] Running PEFT Algorithm...")
    peft_scheduler = PEFT(dag_data['tasks'], dag_data['processors'])
    peft_res, peft_makespan = peft_scheduler.schedule()
    peft_scheduler.print_performance_metrics(peft_makespan)
    
    # 3. Run HEFT
    print("\n[2] Running HEFT Algorithm...")
    heft_scheduler = HEFT(dag_data['tasks'], dag_data['processors'])
    heft_res, heft_makespan = heft_scheduler.schedule()
    print(f"HEFT Total Makespan: {heft_makespan:.2f}")
